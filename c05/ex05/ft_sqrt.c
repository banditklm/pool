int	ft_sqrt(int nb)
{
	int	x ;

	x = 1;
	if (nb == 0)
	{
		return (0);
	}
	else if (nb == 1)
	{
		return (1);
	}
	while (x <= nb / x)
	{
		if (x * x == nb)
		{
			return (x);
		}
		x++;
	}
	return (0);
}
