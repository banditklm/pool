int	ft_is_prime(int nb)
{
	int	x;

	if (nb == 0)
		return (0);
	else if (nb == 1)
		return (0);
	else if (nb < 0)
		return (0);
	x = nb / 2;
	while (x && x > 1)
	{
		if (nb % x == 0)
		{
			return (0);
		}
		x--;
	}
	return (1);
}
